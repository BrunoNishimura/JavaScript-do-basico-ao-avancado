//Math.max, é com letra maiuscúla pois é um conjunto de funções. Objeto Math com o método max, para verificar quem é o maior número.
//Math.max também é uma biblioteca nativa da linguagem, não precisa fazer o Download!

//max: Busca o maior valor.
let maiorNumero = Math.max(1,5,2,3);
console.log(maiorNumero);

//min: Busca o menor valor.
let menor = Math.min(1,5,2,3);
console.log(menor);

//round: arredonda os número.
let a = Math.round(3.1416);
console.log(a)

//ceil: arredonda para cima.
let b = Math.ceil(3.1416)
console.log(b)



