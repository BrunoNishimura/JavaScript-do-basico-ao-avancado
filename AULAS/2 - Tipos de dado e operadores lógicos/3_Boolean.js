//3.1 Comparações
//Maior que: >
//Menor que: <
//Maior ou igual: >=
//Menor ou igual: <=
//Igual: ==
//Diferente: !=
//Idêntico: ===

console.log(1 > 2); //print: false;
console.log(5 < 10); //print: true;
console.log(3 >= 3); //print: true;
console.log(5 <= 4); //print: false;
console.log(5 == 4); //print: false;
console.log('Matheus' != 'Matheus'); //print: false;
console.log(3 === '3'); //print: false;
console.log(4 === 4); //print: true;